def strassen(A,B):
    m1=(A[0][0]+A[1][1])*(B[0][0]+B[1][1])
    m2=(A[1][0]+A[1][1])*B[0][0]
    m3=(B[0][1]-B[1][1])*A[0][0]
    m4=(B[1][0]-B[0][0])*A[1][1]
    m5=(A[0][0]+A[0][1])*B[1][1]
    m6=(A[1][0]-A[0][0])*(B[0][0]+B[0][1])
    m7=(B[1][0]+B[1][1])*(A[0][1]-A[1][1])
    C=[[m1+m4-m5+m7,m3+m5],[m2+m4],[m1+m3-m2+m6]]
    return C

